-- ════════════════════════════════════════════════════════════════════════════
-- Phase 30 — 카테고리·장르 분류 통일 (2026-05-27)
--
-- 변경 사항:
--   카테고리 = 콘텐츠 형식 (6종): 영화 / 드라마 / 애니메이션 / 다큐멘터리 / 뮤직비디오 / 기타
--   장르 = 작품 분위기·테마 (11종): SF / 액션 / 로맨스 / 공포 / 판타지 / 스릴러 / 드라마 / 코미디 / 자연·풍경 / 추상 / 기타
--
-- 기존 영상 데이터 마이그레이션:
--   - 'AI영화' → '영화'
--   - 'AI드라마' → '드라마'
--   - 'AI애니메이션' → '애니메이션'
--   - 'AI다큐멘터리' → '다큐멘터리'
--   - 'AI뮤직비디오' → '뮤직비디오'
--   - 카테고리에 장르값 들어있는 경우 (예: 'SF', '액션' 등) → 카테고리='기타' + 장르=원래값
--     단, videos.genre 컬럼이 있고 비어있을 때만 이동
--
-- 적용 방법:
--   Supabase Dashboard → SQL Editor → 새 쿼리 ("+") → 본 파일 붙여넣기 → Run
-- ════════════════════════════════════════════════════════════════════════════

-- ────────────────────────────────────────────────────────────────────────────
-- Step 1: 현재 카테고리·장르 분포 확인 (적용 전 스냅샷)
-- ────────────────────────────────────────────────────────────────────────────
SELECT 'before' AS phase, category, genre, COUNT(*) AS rows
FROM public.videos
GROUP BY category, genre
ORDER BY rows DESC;

-- ────────────────────────────────────────────────────────────────────────────
-- Step 2: AI* 접두어 콘텐츠 형식 마이그레이션
-- ────────────────────────────────────────────────────────────────────────────
UPDATE public.videos SET category = '영화'       WHERE category = 'AI영화';
UPDATE public.videos SET category = '드라마'     WHERE category = 'AI드라마';
UPDATE public.videos SET category = '애니메이션' WHERE category = 'AI애니메이션';
UPDATE public.videos SET category = '다큐멘터리' WHERE category = 'AI다큐멘터리';
UPDATE public.videos SET category = '뮤직비디오' WHERE category = 'AI뮤직비디오';

-- ────────────────────────────────────────────────────────────────────────────
-- Step 3: 카테고리 자리에 장르값 들어있는 영상 분리
--   카테고리 컬럼에 'SF', '액션', '로맨스', '공포', '판타지', '드라마', '코미디',
--   '스릴러', '자연/풍경', '자연·풍경', '추상' 이 있으면:
--   - genre 컬럼이 비어있으면 → genre 로 이동
--   - genre 컬럼이 차있으면 → 그대로 두고 category 만 '기타' 로
-- ────────────────────────────────────────────────────────────────────────────
-- 3a) 장르 비어있는 행: 카테고리값을 장르로 이동
UPDATE public.videos
SET genre = category,
    category = '기타'
WHERE category IN ('SF', '액션', '로맨스', '공포', '판타지', '드라마', '코미디',
                   '스릴러', '자연/풍경', '자연·풍경', '추상', '음악', '쇼츠', '광고', '튜토리얼')
  AND (genre IS NULL OR genre = '');

-- 3b) 장르 차있는 행: category 만 '기타' 로
UPDATE public.videos
SET category = '기타'
WHERE category IN ('SF', '액션', '로맨스', '공포', '판타지', '드라마', '코미디',
                   '스릴러', '자연/풍경', '자연·풍경', '추상', '음악', '쇼츠', '광고', '튜토리얼');

-- ────────────────────────────────────────────────────────────────────────────
-- Step 4: 장르 컬럼의 '자연/풍경' 표기 → '자연·풍경' 통일
-- ────────────────────────────────────────────────────────────────────────────
UPDATE public.videos SET genre = '자연·풍경' WHERE genre = '자연/풍경';

-- ────────────────────────────────────────────────────────────────────────────
-- Step 5: 카테고리·장르가 새 분류에 없는 값이면 '기타' 로 기본값 처리
--   (마이그레이션 누락 행 안전망)
-- ────────────────────────────────────────────────────────────────────────────
UPDATE public.videos
SET category = '기타'
WHERE category IS NOT NULL
  AND category NOT IN ('영화', '드라마', '애니메이션', '다큐멘터리', '뮤직비디오', '기타');

UPDATE public.videos
SET genre = '기타'
WHERE genre IS NOT NULL
  AND genre NOT IN ('SF', '액션', '로맨스', '공포', '판타지', '스릴러',
                    '드라마', '코미디', '자연·풍경', '추상', '기타');

-- ────────────────────────────────────────────────────────────────────────────
-- Step 6: 결과 확인 (마이그레이션 후 분포)
-- ────────────────────────────────────────────────────────────────────────────
SELECT 'after' AS phase, category, genre, COUNT(*) AS rows
FROM public.videos
GROUP BY category, genre
ORDER BY rows DESC;

